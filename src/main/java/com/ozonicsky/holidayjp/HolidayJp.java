                Calendar cal = Calendar.getInstance();
                cal.set(calendar.get(Calendar.YEAR), calendar.get(Calendar.MONTH), calendar.get(Calendar.DATE), 0, 0, 0);
                cal.add(Calendar.DATE, -1);
                return check(cal);
